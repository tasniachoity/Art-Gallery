    private void btnRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText();
        String full_name = txtFullName.getText();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();
        String password = txtPass.getText();

        if ("".equals(username) && "".equals(full_name)) {
            JOptionPane.showMessageDialog(this, "Fill all the fields");

        } else {
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con1 = DriverManager.getConnection("jdbc:mysql://localhost/artgallery", "root", "");
                insert = con1.prepareStatement("insert into user(username, full_name, phone, email, password) values(?,?,?,?,?)");
                insert.setString(1, username);
                insert.setString(2, full_name);
                insert.setString(3, phone);
                insert.setString(4, email);
                insert.setString(5, password);
                insert.executeUpdate();

                JOptionPane.showMessageDialog(this, "Registered");

                txtUsername.setText("");
                txtFullName.setText("");
                txtPhone.setText("");
                txtEmail.setText("");
                txtPass.setText("");
                txtUsername.requestFocus();

                dispose();
                Login hm = new Login();
                hm.setVisible(true);

            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_btnRegActionPerformed

